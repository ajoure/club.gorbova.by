/**
 * Get episode summary or generate fallback from question titles
 */
export function getEpisodeSummary(
  episodeNumber: number,
  questionTitles?: string[]
): string {
  // Use predefined summary if available
  const predefined = EPISODE_SUMMARIES[episodeNumber];
  if (predefined) return predefined;

  // Fallback: generate from question titles (take first 4-5 unique topics)
  if (questionTitles && questionTitles.length > 0) {
    const uniqueTitles = [...new Set(questionTitles)].slice(0, 5);
    return uniqueTitles.join("; ") + ".";
  }

  return `Ответы на вопросы подписчиков — выпуск №${episodeNumber}`;
}
